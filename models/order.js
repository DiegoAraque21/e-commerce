const mongoose = require("mongoose");

const Schema = mongoose.Schema;

// order schema
const orderSchema = new Schema({
  products: [
    {
      product: {
        type: Object,
        required: true,
      },
      quantity: {
        type: Number,
        required: true,
      },
    },
  ],
  user: {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    userId: {
      type: Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  total: {
    type: Number,
    required: true,
  },
});

module.exports = mongoose.model("Order", orderSchema);
